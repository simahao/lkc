char *getenv(const char *name) {
    char **p = environ;
    size_t len = strlen(name);
    for (; *p != NULL; p++) {
        if (strncmp(name, *p, len) == 0 && (*p)[len] == '=') {
            return &((*p)[len+1]);
        }
    }
    return NULL;
}

